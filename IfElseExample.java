package chapter;

public class IfElseExample {

	public static void main(String[] args) {

		int score = 85;
		
		if(score>=90) {
			System.out.println("점수가 90점보다 크다.");
			System.out.println("등급은 A입니다.");
		}else {
			System.out.println("점수가 90점보다 작다");
			System.out.println("등급은 B입니다.");
		}
		
		
	}

}
