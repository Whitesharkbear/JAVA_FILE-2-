package chapter;

public class SwitchStringExample {

	public static void main(String[] args) {

		String position = "과장";
		
		switch(position) {
		case "과장":
			System.out.println("700만원");
			break;
		case "차장":
			System.out.println("800만원");
			break;
		case "부장":
			System.out.println("900만원");
			break;
			
	
		}
				
				
	}

}
